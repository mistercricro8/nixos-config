-- Hyprland configuration (Lua) — https://wiki.hypr.land/Configuring/Start/

local mocha = require("mocha")
local mauve = "0xff" .. mocha.mauveAlpha
local flamingo = "0xff" .. mocha.flamingoAlpha
local subtext0 = "0xff" .. mocha.subtext0Alpha

hl.config({ autogenerated = false })

hl.on("hyprland.start", function()
  hl.exec_cmd("dbus-update-activation-environment --systemd DISPLAY WAYLAND_DISPLAY XDG_CURRENT_DESKTOP")
  hl.exec_cmd("systemctl --user start hyprland-session.target")
  hl.exec_cmd("systemctl --user start hyprpolkitagent")
  hl.exec_cmd("cliphist")
  hl.exec_cmd("wl-paste --type text --watch cliphist store")
  hl.exec_cmd("wl-paste --type image --watch cliphist store")
  hl.exec_cmd("mutagen daemon start")
end)

-- Global Variables / Environment
hl.env("QT_QPA_PLATFORM", "wayland")
hl.env("ELECTRON_OZONE_PLATFORM_HINT", "auto")
hl.env("QT_QPA_PLATFORMTHEME", "gtk3")
hl.env("QT_QPA_PLATFORMTHEME_QT6", "gtk3")

