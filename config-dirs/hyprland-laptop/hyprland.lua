    blur = {
      enabled = false,
    },
    shadow = {
      enabled = false,
    },
  },
  misc = {
    disable_hyprland_logo = true,
    disable_splash_rendering = true,
    force_default_wallpaper = 0,
    key_press_enables_dpms = false,
    mouse_move_enables_dpms = true,
  },
  dwindle = {
    preserve_split = true,
  },
  xwayland = {
    force_zero_scaling = true,
  },
  debug = {
    disable_logs = false,
    disable_scale_checks = true,
  },
})

